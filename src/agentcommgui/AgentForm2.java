    public void populateAgentsListOnGUI(){
        if (myAgent.agentsList!=null){
            String[]agentsArray = new String[myAgent.agentsList.size()];
            for (int i=0;i<agentsArray.length;i++){
                agentsArray[i]=myAgent.agentsList.get(i).getName();
            }
            comboBoxAgensList.setModel(new javax.swing.DefaultComboBoxModel(agentsArray));
        }
    }
    
