    private void buttonSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSendActionPerformed
        String messageContent = textFieldContent.getText();
        int messagePerf = comboBoxMessagePerf.getSelectedIndex();        
        int receiverIndex = comboBoxAgensList.getSelectedIndex();
        AID receiverAID = myAgent.agentsList.get(receiverIndex);
        //String receiver = (String)comboBoxAgensList.getSelectedItem();
        
        ACLMessage msg = new ACLMessage(messagePerf);
       // msg.addReceiver(new AID(receiver, AID.ISLOCALNAME)); //here the name of the agent is already known
        msg.addReceiver(receiverAID);
        msg.setLanguage("English");
        msg.setContent(messageContent);

        SendMessage sm = new SendMessage(msg, myAgent);
        myAgent.addBehaviour(sm);

    }//GEN-LAST:event_buttonSendActionPerformed

    private void buttonRefreshAgentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRefreshAgentsActionPerformed
        try {
            myAgent.RefetchAgentsList();
        } catch (FIPAException ex) {
            Logger.getLogger(AgentForm2.class.getName()).log(Level.SEVERE, null, ex);
        }
        populateAgentsListOnGUI();
        
    }//GEN-LAST:event_buttonRefreshAgentsActionPerformed

    private void buttonAddRemoteAgentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddRemoteAgentsActionPerformed
        new Thread(new Runnable() {
            @Override
            public void run() {
                String remotePlatformName =  textFieldRemotePlatformName.getText();
                String remotePlatformAddress = textfieldRemoteAddress.getText();
                AID remoteDF = new AID(remotePlatformName,AID.ISGUID);
                remoteDF.addAddresses(remotePlatformAddress);
                myAgent.remoteDF = remoteDF;
                try {
                    myAgent.RefetchAgentsList();
                    populateAgentsListOnGUI();
                } catch (FIPAException ex) {
                    Logger.getLogger(AgentForm2.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }).start();
        
    }//GEN-LAST:event_buttonAddRemoteAgentsActionPerformed

